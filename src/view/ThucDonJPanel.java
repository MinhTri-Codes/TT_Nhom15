public void displayImage(String imgPath) {
    if (imgPath == null || imgPath.isEmpty()) {
        jLabel_IMG.setIcon(null);
        jLabel_IMG.setText("No Image");
        return;
    }
    try {
        // Ưu tiên tìm trong thư mục dự án trước
        File file = new File(imgPath);
        if (!file.exists()) {
            file = new File(System.getProperty("user.dir") + File.separator + imgPath);
        }

        if (file.exists()) {
            ImageIcon imageIcon = new ImageIcon(file.getAbsolutePath());
            
            // CỐ ĐỊNH KÍCH THƯỚC: Thay 180 bằng kích thước Label bạn muốn
            int width = 180; 
            int height = 180;

            Image image = imageIcon.getImage().getScaledInstance(width, height, Image.SCALE_SMOOTH);
            jLabel_IMG.setIcon(new ImageIcon(image));
            jLabel_IMG.setText(""); 
        } else {
            jLabel_IMG.setIcon(null);
            jLabel_IMG.setText("File not found");
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}
private String saveImageToFolder(String sourcePath) {
    try {
        if (sourcePath == null || sourcePath.isEmpty()) return "";

        File sourceFile = new File(sourcePath);
        // Tạo thư mục "img" nếu chưa tồn tại trong dự án
        Path folderPath = Paths.get("img");
        if (!Files.exists(folderPath)) {
            Files.createDirectories(folderPath);
        }

        // Tạo đường dẫn đích: img/tên_file_gốc
        Path targetPath = folderPath.resolve(sourceFile.getName());
        
        // Thực hiện copy file (ghi đè nếu trùng tên)
        Files.copy(sourceFile.toPath(), targetPath, StandardCopyOption.REPLACE_EXISTING);
        
        // Trả về đường dẫn tương đối để lưu vào DB (ví dụ: img/cf_den.jpg)
        return "img/" + sourceFile.getName();
    } catch (Exception e) {
        e.printStackTrace();
        return sourcePath; // Nếu lỗi thì trả về đường dẫn cũ
    }
}
private void deleteProductImage(String imgPath) {
    if (imgPath == null || imgPath.isEmpty()) {
        return;
    }
    
    try {
        // Tạo đối tượng File từ đường dẫn lưu trong DB
        File file = new File(imgPath);
        
        // Nếu đường dẫn trong DB là tương đối (img/abc.jpg), cần trỏ đúng vào thư mục dự án
        if (!file.isAbsolute()) {
            file = new File(System.getProperty("user.dir") + File.separator + imgPath);
        }
        
        // Kiểm tra và thực hiện xóa
        if (file.exists()) {
            if (file.delete()) {
                System.out.println("Đã xóa ảnh: " + file.getAbsolutePath());
            } else {
                System.err.println("Không thể xóa ảnh (có thể tệp đang được sử dụng).");
            }
        }
    } catch (Exception e) {
        System.err.println("Lỗi khi xóa ảnh: " + e.getMessage());
    }
}

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
     int row = tbSanPham.getSelectedRow();
    if (row < 0) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn món cần sửa!");
        return;
    }

    try {
        int maSP = (int) tableModel.getValueAt(row, 0);
        String ten = txtTenSP.getText().trim();
        double gia = Double.parseDouble(txtDonGia.getText().trim());
        String img = txtIMG_URL.getText().trim();
        int maDM = cboDanhMuc.getSelectedIndex() + 1;

        SanPham sp = new SanPham(maSP, ten, gia, maDM, img);
        if (sanPhamDAO.updateSanPham(sp)) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            loadDataToTable();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi dữ liệu!");
    }   
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
       int row = tbSanPham.getSelectedRow();
    if (row >= 0) {
        int id = (int) tableModel.getValueAt(row, 0);
        
        // 1. Lấy thông tin sản phẩm để biết đường dẫn ảnh trước khi xóa khỏi DB
        SanPham sp = sanPhamDAO.getSanPhamById(id);
        
        if (sp != null) {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa sản phẩm này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            
            if (confirm == JOptionPane.YES_OPTION) {
                // 2. Xóa sản phẩm trong Database
                if (sanPhamDAO.deleteSanPham(id)) {
                    
                    // 3. Xóa tệp ảnh vật lý
                    deleteProductImage(sp.getImgUrl());
                    
                    JOptionPane.showMessageDialog(this, "Xóa sản phẩm và ảnh thành công!");
                    loadDataToTable();
                    resetForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại!");
                }
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần xóa trên bảng!");
    }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
     try {
        String ten = txtTenSP.getText().trim();
        String giaStr = txtDonGia.getText().trim();
        String sourceImgPath = txtIMG_URL.getText().trim(); // Đường dẫn tuyệt đối từ JFileChooser

        if(ten.isEmpty() || giaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên và giá!");
            return;
        }

        // BƯỚC QUAN TRỌNG: Copy ảnh vào folder dự án và lấy đường dẫn tương đối
        String relativePath = saveImageToFolder(sourceImgPath);

        // Lấy MaDM từ Database (đã cập nhật ở bước trước)
        int maDM = 1;
        String tenDMSelected = cboDanhMuc.getSelectedItem().toString();
        dao.DanhMucDAO dmDAO = new dao.DanhMucDAO();
        for(model.DanhMuc dm : dmDAO.getAllDanhMuc()) {
            if(dm.getTenDanhMuc().equals(tenDMSelected)) {
                maDM = dm.getMaDM();
                break;
            }
        }

        // Lưu vào Database với relativePath (ví dụ: img/hinh_anh.jpg)
        SanPham sp = new SanPham(0, ten, Double.parseDouble(giaStr), maDM, relativePath);
        if (sanPhamDAO.addSanPham(sp)) {
            JOptionPane.showMessageDialog(this, "Thêm món và lưu ảnh thành công!");
            loadDataToTable();
            resetForm();
        }
    } catch (Exception e) { 
        JOptionPane.showMessageDialog(this, "Lỗi hệ thống: " + e.getMessage()); 
    }
    }//GEN-LAST:event_btnThemActionPerformed


    private void btnTimKiemIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemIDActionPerformed
String idText = txtTimKiemID.getText().trim();
    if (idText.isEmpty()) {
        loadDataToTable(); // Nếu trống thì hiện tất cả
        return;
    }

    try {
        int id = Integer.parseInt(idText);
        SanPham sp = sanPhamDAO.getSanPhamById(id);
        if (sp != null) {
            // Đổ lên form
            txtTenSP.setText(sp.getTenSP());
            txtDonGia.setText(String.valueOf(sp.getDonGia()));
            txtIMG_URL.setText(sp.getImgUrl());
            cboDanhMuc.setSelectedIndex(sp.getMaDM() - 1);
            displayImage(sp.getImgUrl()); // Hiện ảnh
            
            // Chọn dòng đó trên bảng
            for (int i = 0; i < tbSanPham.getRowCount(); i++) {
                if ((int)tbSanPham.getValueAt(i, 0) == id) {
                    tbSanPham.setRowSelectionInterval(i, i);
                    break;
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm có ID: " + id);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID phải là số!");
    }
    }//GEN-LAST:event_btnTimKiemIDActionPerformed

    private void tbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSanPhamMouseClicked
      int row = tbSanPham.getSelectedRow();
        if (row >= 0) {
            int maSP = (int) tableModel.getValueAt(row, 0);
            SanPham sp = sanPhamDAO.getSanPhamById(maSP);
            if (sp != null) {
                txtTenSP.setText(sp.getTenSP());
                txtDonGia.setText(String.valueOf(sp.getDonGia()));
                txtIMG_URL.setText(sp.getImgUrl());
                cboDanhMuc.setSelectedItem(tableModel.getValueAt(row, 2));
                displayImage(sp.getImgUrl()); // HIỂN THỊ ẢNH KHI CLICK DÒNG
            }
        }
    }//GEN-LAST:event_tbSanPhamMouseClicked

